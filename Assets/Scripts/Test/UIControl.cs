using TMPro;
using UnityEngine;

public class UIControl : MonoBehaviour
{
    public static bool isUINow; //UIを開いているか判別するトークン

    public GameObject ui, parents;   //UI全体の格納

    TextMeshProUGUI[] tMP;

    int nowIndex = 0;   //現在のカーソル位置

    void Start()
    {
        tMP = parents.GetComponentsInChildren<TextMeshProUGUI>();

        tMP[nowIndex].color = Color.yellow;

        //UIを非表示にする
        isUINow = false;
        ui.SetActive(isUINow);
    }

    void Update()
    {
        //Escを押すとマップに戻る
        if (Input.GetKeyDown(KeyCode.Escape))
        {
            if (isUINow)
            {
                tMP[nowIndex].color = Color.white;
                nowIndex = 0;
                tMP[nowIndex].color = Color.yellow;


                isUINow = false;
                ui.SetActive(isUINow);
            }
        }

        //Aまたは←を押すと左の要素に移る
        if (Input.GetKeyDown(KeyCode.A) || Input.GetKeyDown(KeyCode.LeftArrow))
        {
            tMP[nowIndex].color = Color.white;

            if (nowIndex == 0)
            {
                nowIndex = tMP.Length - 1;
            }
            else
            {
                nowIndex -= 1;
            }

            tMP[nowIndex].color = Color.yellow;
        }

        //Dまたは→を押すと右の要素に移る
        if (Input.GetKeyDown(KeyCode.D) || Input.GetKeyDown(KeyCode.RightArrow))
        {
            tMP[nowIndex].color = Color.white;

            if (nowIndex == tMP.Length - 1)
            {
                nowIndex = 0;
            }
            else
            {
                nowIndex += 1;
            }

            tMP[nowIndex].color = Color.yellow;
        }
    }
}
